function obterLinguagem (lang) {
    if(lang === "es") {
        console.log("Hola Mundo!")
    } else if (lang === "en") {
        console.log("Hello World!")
    } else if (lang === "pt") {
        console.log("Olá Mundo!")
    } else {
        console.log("Olá Mundo")
    }
}

obterLinguagem("es")
obterLinguagem("en")
obterLinguagem("pt")
obterLinguagem("aa")